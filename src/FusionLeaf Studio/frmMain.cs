        private void RestoreToolStripMenuItemClick(object sender, EventArgs e)
        {
        	if (!processIsRunning("mysqld"))
        	{
        		MessageBox.Show("MySQL must be running to restore","MySQL Problem");
        		return;
        	}
            string strHidden = Path.Combine(strHomeDir,@"webroot\localhost\_hidden");
            string strFile = Path.Combine(strHidden,"site");
            
            if (File.Exists(strFile))
            {            	
            	string strMySQLBackup = Path.Combine(strHidden,"mysql_backup");
            	if (!Directory.Exists(strMySQLBackup))
            	{
            		MessageBox.Show("No backup folder found","Restore Problem");
            		return;
            	}
            	string[] strFiles = Directory.GetFiles(strMySQLBackup);
            	
            	if (strFiles.Length>0)
            	{
	            	string strNewestRestore = strFiles[strFiles.Length-1];
	            	FileInfo fi = new FileInfo(strNewestRestore);
	            	string strDBUser = grabValue(strFile,"db_root_username");
	            	string strDBPass = grabValue(strFile,"db_root_password");
	            	string strDBDatabase = grabValue(strFile,"db_database");
	            	
	            	string strCmd = Path.Combine(folderMySQL,"mysql.exe");
	            	string strArgs = "--user "+strDBUser+" --password="+strDBPass+" --port="
	            		+numMySQLPort.Value+" "+strDBDatabase+" < \""+strNewestRestore+"\"";
	            	DialogResult dr = MessageBox.Show("Are you sure you want to restore the latest backup?\r\n\r\n"+fi.Name,"Confirm Restore",
	            	                                  MessageBoxButtons.YesNo,MessageBoxIcon.Question);
	            	if (dr== DialogResult.Yes)
	            	{
	            		string strTempFile = "temp.cmd";
	            		File.WriteAllText(strTempFile,"@ECHO OFF\r\n\""+strCmd+"\" "+strArgs+"\r\ndel temp.cmd");
	            		runCmd(strTempFile,"","");
	            		MessageBox.Show("Restore finished. Be sure to run a /recache","Complete");
	            		if (File.Exists(strTempFile)) File.Delete(strTempFile);
	            		
	            	}else MessageBox.Show("Restore canceled","No Change");
            	}
            	else MessageBox.Show("No backup files found","Restore Problem");
            	
            }
            else MessageBox.Show("Cannot read site info","File Problem");
        }

