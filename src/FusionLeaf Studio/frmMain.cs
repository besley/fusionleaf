        		}
        		
        		txtStatus.Text = "FastCGI started "+numFCGIThreads.Value+" thread(s).";
        		if (!blFCGIThreadRun)
        		{
        			blFCGIThreadRun = true;
        			tFCGI = new Thread(new ThreadStart(monitorFCGI));
        			tFCGI.IsBackground=true;
        			tFCGI.Start();
        			
        			//txtStatus.Text = "FastCGI monitoring thread started.";
