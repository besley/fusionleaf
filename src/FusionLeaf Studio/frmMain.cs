        Thread tFCGI;
        bool blFCGIThreadRun = false;
        bool blFCGIRunning = false;
        bool blFCGIKill = false;
        
        Hashtable htFCGI;
        
		public void monitorFCGI()
        {        	
        	int intStartCount = (int)numFCGIThreads.Value;
        	Hashtable htLocal = new Hashtable();
        	
			foreach(Object o in htFCGI.Keys)
			{
				htLocal.Add(o,htFCGI[o]);
			}
        	
        	while(blFCGIRunning || !blFCGIKill)
        	{
	        	blFCGIRunning = processIsRunning("php-cgi");
	        	if (blFCGIRunning) txtFCGIStatus.Invoke((MethodInvoker)delegate(){this.txtFCGIStatus.Text = "Running "+processIsRunningCount("php-cgi")+" thread(s)";});
	        	else txtFCGIStatus.Invoke((MethodInvoker)delegate(){this.txtFCGIStatus.Text = "Stopped";});
	        	
	        	if (intStartCount!=processIsRunningCount("php-cgi") && blFCGIKill==false)
	        	{
	        		Process[] pLIst = System.Diagnostics.Process.GetProcessesByName("php-cgi");
	        		
	        		Hashtable htTemp = (Hashtable)htLocal.Clone();
	        		
	        		foreach(Object o in htTemp.Keys)
	        		{
	        			bool blFound = false;
		        		foreach(Process p in pLIst)
		        		{
		        			if ((int)o==p.Id)
		        			{
		        				blFound=true;
		        				break;
		        			}
		        		}
		        		if (!blFound)
		        		{
		        			int intPID = runCmd(fileFCGI,"-b 127.0.0.1:" + htTemp[o].ToString(), folderFCGI);
		        			htLocal.Remove(o);
		        			htLocal.Add(intPID,(int)htTemp[o]);
		        			txtStatus.Text = "FastCGI thread restarting on 127.0.0.1:"+htTemp[o].ToString()+" with PID "+intPID.ToString()+".";
		        		}
	        		}
	        	}
	        	Thread.Sleep(1000);
	        		
        	}
        	txtStatus.Text = "FastCGI threads ended.";
        	blFCGIThreadRun = false;
        }
        
